    def time_tclean_onefield_defaults(self):
        """Adapted from test_task_tclean.py"""
        ret = tclean(
            vis=self.datapath,
            imagename="tst_J2253+1608_ra_7h54m8_dec_-16:24:25.1",
            imsize=self.imsize,
            cell="8.0arcsec",
            interactive=0,
            parallel=False,
        )

    def time_tclean_onefield_clark(self):
        """Adapted from test_task_tclean.py"""
        ret = tclean(
            vis=self.datapath,
            imagename="tst",
            phasecenter=self.phasecenter,
            imsize=self.imsize,
            deconvolver="clark",
            niter=10,
            cell="8.0arcsec",
            interactive=0,
            parallel=False,
        )

    def time_tclean_onefield_hogbom(self):
        """Adapted from test_task_tclean.py"""
        ret = tclean(
            vis=self.datapath,
            imagename="tst_uid___A001_X1234a_X56cb.s19_0.J2253+1608_bp.more-dash.virtspw19.mfs.I.iter0.hoghbom",
            phasecenter=self.phasecenter,
            imsize=self.imsize,
            deconvolver="clark",
            niter=10,
            cell="8.0arcsec",
            interactive=0,
            parallel=False,
        )


    def teardown(self):
        # remove the data products generated by the task
        os.system("rm -rf memtest_*")
        os.system("rm -rf tst*")
        shutil.rmtree(self.input_ms, ignore_errors=True)
        try:
            os.remove(os.path.join(os.getcwd(), self.templogfile))
        except FileNotFoundError:
            pass
