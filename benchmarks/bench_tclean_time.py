    input_ms = "refim_twochan.ms"

    # assign our test dataset
    datapath = os.path.join(dataroot, input_ms)

    templogfile = "tclean_memprofile.log"

    imsize = 100
    phasecenter = "J2000 19h59m57.5s +40d49m00.077s"

